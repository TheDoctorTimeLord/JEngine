package ru.jengine.utils;

import com.google.common.base.Objects;

public class Pair<K, V> {
    private K key;
    private V value;

    public Pair(K key, V value) {
        this.key = key;
        this.value = value;
    }

    public K getKey() {
        return key;
    }

    public V getValue() {
        return value;
    }

    public void setKey(K key) {
        this.key = key;
    }

    public void setValue(V value) {
        this.value = value;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o)
            return true;
        if (o == null || getClass() != o.getClass())
            return false;
        Pair<?, ?> pair = (Pair<?, ?>)o;
        return Objects.equal(key, pair.key) && Objects.equal(value,
                pair.value);
    }

    @Override
    public int hashCode() {
        return Objects.hashCode(key, value);
    }
}